namespace ExerciciosPraticos3;
public class Operacoes
{
    public double Soma(double valor1, double valor2) => valor1 + valor2;
    public double Subtracao(double valor1, double valor2) => valor1 - valor2;
    public double Multiplicacao(double valor1, double valor2) => valor1 * valor2;
    public double Media(double valor1, double valor2) => (valor1 + valor2) / 2;
    public double Divisao(double valor1, double valor2) => valor1 / valor2;
}
